package com.neotech.lesson03;

public class StringExample {

	public static void main(String[] args) {
		
	//primitive data types:    data type     name     =     value;
		//byte, short, int, long, long, float, double, char, and boolean
		
		// non-primitive data types: data type     name    =    value;
		//non-primitive is a sequence of characters
		//char can only use one character
		
		int a;
		a = 10;
		
		int b = 15;
		
		char c = 'g';
		
		//what is the type, how do I write it?
		String schoolName = "Neotech";
		String name = "Bello";
		int age = 25;
		String income = "100k";      //anything inside the double quotes is a string   //string is a series of characters
		String nationality = "Albanian";
		
		
		System.out.println(schoolName);
		System.out.println(name);
		System.out.println(age);
		System.out.println(income);
		System.out.println(nationality);
		
		//reassign nationality
		nationality = "Nigerian";
		

	}

}
